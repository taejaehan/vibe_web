// ------------------------------
// 기본 설정
// ------------------------------
const SPRITE_W = 50
const SPRITE_H = 50
const FPS = 7.5
const PIXEL_SIZE = 6

// 팔레트
const PALETTE = [
  '#000000', // 0 background
  '#f5f5f5', // 1 body
  '#5a5a5a', // 2 shadow
  '#141414', // 3 outline
]

// ------------------------------
// RLE 디코더
// ------------------------------
function decodeRLE(rle) {
  const out = []
  for (let i = 0; i < rle.length; i++) {
    const [count, value] = rle[i]
    for (let j = 0; j < count; j++) {
      out.push(value)
    }
  }
  return out
}

// ------------------------------
// 1차원 배열 -> 2차원 배열
// ------------------------------
function toGrid(flat, w, h) {
  const grid = []
  for (let y = 0; y < h; y++) {
    grid.push(flat.slice(y * w, (y + 1) * w))
  }
  return grid
}

// ------------------------------
// 픽셀 스프라이트 그리기
// ------------------------------
function drawPixelSprite(ctx, grid, cx, cy, pixelSize) {
  const drawW = SPRITE_W * pixelSize
  const drawH = SPRITE_H * pixelSize
  const startX = Math.floor(cx - drawW / 2)
  const startY = Math.floor(cy - drawH / 2)

  for (let y = 0; y < SPRITE_H; y++) {
    for (let x = 0; x < SPRITE_W; x++) {
      const colorIndex = grid[y][x]
      if (colorIndex === 0) continue

      ctx.fillStyle = PALETTE[colorIndex]
      ctx.fillRect(
        startX + x * pixelSize,
        startY + y * pixelSize,
        pixelSize,
        pixelSize
      )
    }
  }
}

